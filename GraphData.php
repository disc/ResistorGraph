    public static function getMatrix() {
        $matrix = array();
        $nodesCount = count(self::getNodeLabelList());
        for ($i=0; $i<$nodesCount;$i++) {
            $matrix[] = array_fill(0, $nodesCount, 0);
        }

        // fill the matrix
        foreach (self::getNodeList() as $node => $neighbors) {
            $nodeIndex = array_search($node, self::getNodeLabelList(), null);
            foreach ($neighbors as $neighborNode => $value) {
                $neighborNodeIndex = array_search($neighborNode, self::getNodeLabelList(), null);
                $matrix[$nodeIndex][$neighborNodeIndex] = $value;
            }
        }

        return $matrix;
    }

